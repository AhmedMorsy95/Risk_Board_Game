    static ArrayList<Integer> getNeighbours(int node, int boardSize) {
        if (boardSize == 27) {
            
            ArrayList<Integer> x = new ArrayList<Integer>();

            for (int i = 0; i < boardSize; i++) {
                if (egyptMap[node][i] == 1) {
                    x.add(i);
                }
            }

            return x;
        } else if (boardSize == 50) {
            ArrayList<Integer> x = new ArrayList<Integer>();

            for (int i = 0; i < boardSize; i++) {
                if (usaMap[node][i] == 1) {
                    x.add(i);
                }
            }

            return x;
        }

        return null;
    }
